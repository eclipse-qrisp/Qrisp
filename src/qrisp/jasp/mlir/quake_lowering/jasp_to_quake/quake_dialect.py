@irdl_op_definition
class SubVeqOp(IRDLOperation):
    """Slice a veq: ``quake.subveq %veq, %lo, %hi : (!quake.veq<?>, i64, i64) -> !quake.veq<?>``."""

    name = "quake.subveq"
    veq = operand_def(QuakeVeqType)
    lo = operand_def(i64)
    hi = operand_def(i64)
    result = result_def(QuakeVeqType)

    def __init__(self, veq: SSAValue, lo: SSAValue, hi: SSAValue) -> None:
        super().__init__(operands=[veq, lo, hi], result_types=[QuakeVeqType()])

    def print(self, printer: Printer) -> None:
        printer.print_string(" ")
        printer.print_ssa_value(self.veq)
        printer.print_string(", ")
        printer.print_ssa_value(self.lo)
        printer.print_string(", ")
        printer.print_ssa_value(self.hi)
        # Use functional-type format: (veq_type, lo_type, hi_type) -> result_type
        printer.print_string(" : (")
        printer.print_attribute(self.veq.type)
        printer.print_string(", ")
        printer.print_attribute(self.lo.type)
        printer.print_string(", ")
        printer.print_attribute(self.hi.type)
        printer.print_string(") -> !quake.veq<?>")


@irdl_op_definition
class ConcatOp(IRDLOperation):
    """Concatenate qubit references: ``quake.concat %a, %b : (...) -> !quake.veq<?>``."""

    name = "quake.concat"
    operands_ = var_operand_def(AnyAttr())
    result = result_def(QuakeVeqType)

    def __init__(self, operands: Sequence[SSAValue]) -> None:
        super().__init__(operands=[operands], result_types=[QuakeVeqType()])

    def print(self, printer: Printer) -> None:
        printer.print_string(" ")
        printer.print_list(self.operands_, printer.print_ssa_value)
        printer.print_string(" : (")
        printer.print_list(self.operands_, lambda v: printer.print_attribute(v.type))
        printer.print_string(") -> !quake.veq<?>")


# ---------------------------------------------------------------------------
# Quantum gate ops (memory semantics – all side-effecting, no results)
# ---------------------------------------------------------------------------


def _quake_gate_print(self: IRDLOperation, printer: Printer) -> None:  # noqa: N802
    """Shared print logic for all Quake gate ops in memory semantics mode.

    Format (following Quake assembly format)::

        quake.h [%ctrl0] (%param0) %tgt : (param-types, ctrl-types, tgt-types) -> ()

    The type signature uses MLIR's ``functional-type`` format (flat list of all
    SSA operand types in order: parameters, controls, targets) as defined by
    the CUDA-Q Quake dialect's ``AttrSizedOperandSegments`` assemblyFormat.
    """

    # Float parameters
    param_list = list(self.params)
    if param_list:
        printer.print_string(" (")
        printer.print_list(param_list, printer.print_ssa_value)
        printer.print_string(")")

    # Controls
    ctrl_list = list(self.controls)
    if ctrl_list:
        printer.print_string(" [")
        printer.print_list(ctrl_list, printer.print_ssa_value)
        printer.print_string("]")

    # Target qubits (always at least one)
    tgt_list = list(self.targets)
    if tgt_list:
        printer.print_string(" ")
        printer.print_list(tgt_list, printer.print_ssa_value)

    # Type signature: functional-type over ALL SSA operands (params, controls, targets)
    # This matches CUDA-Q's assemblyFormat which uses functional-type(operands, results).
    all_operands = param_list + ctrl_list + tgt_list
    printer.print_string(" : (")
    printer.print_list(all_operands, lambda v: printer.print_attribute(v.type))
    printer.print_string(") -> ()")


def _mk_gate_cls(quake_name: str) -> type:
    """Create an xDSL IRDLOperation subclass for the Quake gate *quake_name*.

    All gate classes share the same operand layout (controls, params, targets)
    and the same :func:`_quake_gate_print` printer.

    Parameters
    ----------
    quake_name:
        Fully-qualified Quake gate name, e.g. ``"quake.h"`` or ``"quake.rx"``.

    Returns
    -------
    type
        A new, irdl-registered subclass of :class:`IRDLOperation`.
    """
    safe_name = quake_name.replace(".", "_").replace("<", "_").replace(">", "")

    ns: dict = {
        "name": quake_name,
        "irdl_options": (AttrSizedOperandSegments(),),
        "controls": var_operand_def(AnyAttr()),
        "params": var_operand_def(AnyAttr()),
        "targets": var_operand_def(AnyAttr()),
        "print": _quake_gate_print,
    }
    cls = type(f"_Quake_{safe_name}Op", (IRDLOperation,), ns)
    return irdl_op_definition(cls)


def _gate_init(
    cls: type,
    controls: Sequence[SSAValue],
    params: Sequence[SSAValue],
    targets: Sequence[SSAValue],
) -> IRDLOperation:
    """Instantiate a Quake gate op (used by the factory helper :func:`make_gate_op`)."""
    return cls(operands=[list(controls), list(params), list(targets)])


# Pre-built gate op classes indexed by Quake gate name (after stripping "quake." prefix).
# e.g. "h" → QuakeHGateOp class, "s<adj>" → QuakeSAdjGateOp class
_QUAKE_GATE_CLASSES: dict[str, type] = {}

for _gname in [
    # 1-qubit, 0-param
    "h",
    "x",
    "y",
    "z",
    "s",
    "t",
    # 1-qubit, 1-param
    "rx",
    "ry",
    "rz",
    "r1",
    # 1-qubit, 3-param
    "u3",
    # 2-qubit, 0-param
    "swap",
    # Adjoint forms
    "s<adj>",
    "t<adj>",
]:
    _QUAKE_GATE_CLASSES[_gname] = _mk_gate_cls(f"quake.{_gname}")


def make_gate_op(
    gate_name: str,
    controls: Sequence[SSAValue],
    params: Sequence[SSAValue],
    targets: Sequence[SSAValue],
) -> IRDLOperation | None:
    """Instantiate a Quake gate op for the given Jasp *gate_name*.

    Parameters
    ----------
    gate_name:
        Jasp gate name (e.g. ``"h"``, ``"cx"``, ``"rz"``).
    controls:
        SSA values for the control qubits (may be empty).
    params:
        SSA values for the floating-point gate parameters (may be empty).
    targets:
        SSA values for the target qubits.

    Returns
    -------
    IRDLOperation | None
        A new Quake gate op instance, or *None* if the gate is not supported.
    """
    from qrisp.jasp.mlir.quake_lowering.jasp_to_quake.gate_mapping import GATE_MAP

    gate_info = GATE_MAP.get(gate_name)
    if gate_info is None:
        return None

    quake_gate = gate_info.quake_gate  # e.g. "x" for "cx", "s<adj>" for "s_dg"
    cls = _QUAKE_GATE_CLASSES.get(quake_gate)
    if cls is None:
        return None

    return _gate_init(cls, controls, params, targets)


