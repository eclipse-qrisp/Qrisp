@irdl_attr_definition
class QuakeRefType(ParametrizedAttribute, TypeAttribute):
    """Quake qubit reference type ``!quake.ref`` (memory semantics)."""

    name = "quake.ref"


@irdl_attr_definition
class QuakeVeqType(ParametrizedAttribute, TypeAttribute):
    """Quake qubit-register reference type ``!quake.veq<?>`` (dynamic size)."""

    name = "quake.veq"

    def print_parameters(self, printer: Printer) -> None:
        printer.print_string("<?>")


@irdl_attr_definition
class QuakeMeasureType(ParametrizedAttribute, TypeAttribute):
    """Quake single-measurement result type ``!quake.measure``."""

    name = "quake.measure"


# Shorthand aliases used in operand constraints
_QubitOrVeq = AnyAttr()  # accepts both QuakeRefType and QuakeVeqType


