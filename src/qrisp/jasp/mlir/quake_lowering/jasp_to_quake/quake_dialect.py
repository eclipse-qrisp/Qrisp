_ALL_GATE_CLASSES = list(_QUAKE_GATE_CLASSES.values())


class QuakeDialect(Dialect):
    """Minimal xDSL dialect for the CUDA-Q Quake dialect (memory semantics)."""

    name = "quake"
    operations = [
        AllocaRefOp,
        AllocaVeqOp,
        DeallocOp,
        ExtractRefOp,
        VeqSizeOp,
        SubVeqOp,
        ConcatOp,
        MzOp,
        DiscriminateOp,
        ResetOp,
        *_ALL_GATE_CLASSES,
    ]
    attributes = [QuakeRefType, QuakeVeqType, QuakeMeasureType]
