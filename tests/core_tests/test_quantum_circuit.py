def test_mcz_mcp_crz(gate, expected_mat):
    """Verify the gates are applied as expected to a quantum circuit."""
    qc = QuantumCircuit(4)
    qc.cx(0, range(1, 4))
    if gate == "mcz":
        qc.mcz([0, 1, 2, 3])
    elif gate == "mcp":
        qc.mcp(0.1, [0, 1, 2, 3])
    elif gate == "crz":
        qc.crz(0.1, 0, 2)
    calculated_mat = qc.get_unitary()
    np.testing.assert_array_almost_equal(calculated_mat, expected_mat)


